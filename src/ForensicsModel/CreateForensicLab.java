    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        boolean isValid = Validation();
        if (isValid) {
        int forLabId = Integer.parseInt(txtForLabId.getText());
        int hospId = Integer.parseInt(txtHospId.getText());
        String hospName = txtHospName.getText();
              

        ForensicLab f = forLabHistory.addNewForLab();

        f.setForLabId(forLabId);
        f.setHospId(hospId);
        f.setHospName(hospName);
        
        dbConForLabDetails.addForLabDataToDB(f);
        JOptionPane.showMessageDialog(this, "New Forensic Lab is added.");

        txtForLabId.setText("");
        txtHospId.setText("");
        txtHospName.setText("");
    }           
        
    }//GEN-LAST:event_btnAddActionPerformed

