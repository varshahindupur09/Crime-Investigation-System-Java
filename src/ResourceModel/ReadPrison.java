private void populateTable() {
            DefaultTableModel model = (DefaultTableModel) HTable.getModel();
            model.setRowCount(0);

            for(Prison e: prisonHistory.getPrisonHistory()){

                Object[] row = new Object[7];
                row[0] = e;
                row[1] = e.getPrisonName();
                row[2] = e.getOfficerId();
                row[3] = e.getOfficerName();
                row[4] = e.getJailCount();
                row[5] = e.getCity();
                model.addRow(row);
            }
        }

        
