    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
            
            int selectedRowIndex = HTable.getSelectedRow();
            if (selectedRowIndex < 0) {
                JOptionPane.showMessageDialog(this, "Please select a row to update");
                return;
            }

            DefaultTableModel model = (DefaultTableModel) HTable.getModel();
            Prison selectedPrison = (Prison) model.getValueAt(selectedRowIndex, 0);

            int prisonId = Integer.parseInt(txtPrisonId.getText());
            String prisonName = txtPrisonName.getText();
            int officerId = Integer.parseInt(txtOfficerId.getText());
            String officerName = txtOfficerName.getText();
            int jailCount = Integer.parseInt(txtJailCount.getText());
            String city = txtCity.getText();

            JOptionPane.showMessageDialog(this, "Prison Information Updated");
            //history.deleteEmployee(selectedEmployee);
            selectedPrison.setPrisonId(prisonId);
            selectedPrison.setPrisonName(prisonName);
            selectedPrison.setOfficerId(officerId);
            selectedPrison.setOfficerName(officerName);
            selectedPrison.setJailCount(jailCount);
            selectedPrison.setCity(city);
             
            txtPrisonId.setText("");
            txtPrisonName.setText("");
            txtOfficerId.setText("");
            txtOfficerName.setText("");
            txtJailCount.setText("");
            txtCity.setText("");
        
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = HTable.getSelectedRow();

        if (selectedRowIndex<0) {
            JOptionPane.showMessageDialog(this, "Please select a row to view");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) HTable.getModel();
        Prison selectedPrison = (Prison) model.getValueAt(selectedRowIndex, 0);

        txtPrisonId.setText(String.valueOf(selectedPrison.getPrisonId()));
        txtOfficerId.setText(selectedPrison.getPrisonName());
        txtPrisonName.setText(String.valueOf(selectedPrison.getOfficerId()));
        txtJailCount.setText(selectedPrison.getOfficerName());
        txtOfficerName.setText(String.valueOf(selectedPrison.getJailCount()));
        txtJailCount.setText(selectedPrison.getCity());
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = HTable.getSelectedRow();

        if (selectedRowIndex<0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) HTable.getModel();
        Prison selectedPrison = (Prison) model.getValueAt(selectedRowIndex, 0);

        prisonHistory.deletePri(selectedPrison);
        JOptionPane.showMessageDialog(this, "Prison deleted");

        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

