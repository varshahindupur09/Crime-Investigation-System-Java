private void populateTable() {
            DefaultTableModel model = (DefaultTableModel) HTable.getModel();
            model.setRowCount(0);

            for(Weapon e: weaponHistory.getWeaponHistory()){

                Object[] row = new Object[4];
                row[0] = e;
                row[1] = e.getwType();
                row[2] = e.getCount();
                model.addRow(row);
            }
        }

        
