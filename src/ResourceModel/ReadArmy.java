    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
            boolean isValid = Validation();
            if (isValid) {
            int selectedRowIndex = HTable.getSelectedRow();
            if (selectedRowIndex < 0) {
                JOptionPane.showMessageDialog(this, "Please select a row to update");
                return;
            }

            DefaultTableModel model = (DefaultTableModel) HTable.getModel();
            Army selectedArmy = (Army) model.getValueAt(selectedRowIndex, 0);

            int genId = Integer.parseInt(txtGenId.getText());
            String genName = txtGenName.getText();
            String department = txtDep.getText();
            String country = txtCountry.getText();

            JOptionPane.showMessageDialog(this, "Army Information Updated");
            //history.deleteEmployee(selectedEmployee);
            selectedArmy.setGeneralId(genId);
            selectedArmy.setGeneralName(genName);
            selectedArmy.setDepartment(department);
            selectedArmy.setCountry(country);
            
            dbConArmyDetails.updateArmyDataToDB(selectedArmy);
             
            populateTable();
            txtGenId.setText("");
            txtGenName.setText("");
            txtDep.setText("");
            txtCountry.setText("");
            }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = HTable.getSelectedRow();

        if (selectedRowIndex<0) {
            JOptionPane.showMessageDialog(this, "Please select a row to view");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) HTable.getModel();
        Army selectedArmy = (Army)model.getValueAt(selectedRowIndex, 0);

        txtGenId.setText(String.valueOf(selectedArmy.getGeneralId()));
        txtGenName.setText(selectedArmy.getGeneralName());
        txtDep.setText(selectedArmy.getDepartment());
        txtCountry.setText(selectedArmy.getCountry());
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = HTable.getSelectedRow();

        if (selectedRowIndex<0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) HTable.getModel();
        Army selectedArmy = (Army)model.getValueAt(selectedRowIndex, 0);

        armyHistory.deleteArmy(selectedArmy);
        dbConArmyDetails.deleteArmyDataInDB(selectedArmy);
        JOptionPane.showMessageDialog(this, "Army deleted");

        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed


