    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        
        int jailId = Integer.parseInt(txtJailId.getText());
        String jailerId = txtJailerId.getText();
        String jailerName = txtJailerName.getText();
        int crimCount = Integer.parseInt(txtCcount.getText());
       

        Jail j = jailHistory.addNewJail();

        j.setJailId(jailId);
        j.setJailerId(jailId);
        j.setJailerName(jailerName);
        j.setCriminalCount(crimCount);
        
        JOptionPane.showMessageDialog(this, "New Government is added.");

        txtJailId.setText("");
        txtJailerId.setText("");
        txtJailerName.setText("");
        txtCcount.setText("");
          
        
    }//GEN-LAST:event_btnAddActionPerformed

