    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        
        int prisonId = Integer.parseInt(txtPrisonId.getText());
        String prisonName = txtPrisonName.getText();
        int officerId = Integer.parseInt(txtOfficerId.getText());
        String officerName = txtOfficerName.getText();
        int jailCount = Integer.parseInt(txtJailCount.getText());
        String city = txtCity.getText();

        Prison p = prisonHistory.addNewPrison();

        p.setPrisonId(prisonId);
        p.setPrisonName(prisonName);
        p.setOfficerId(officerId);
        p.setOfficerName(officerName);
        p.setJailCount(jailCount);
        p.setCity(city);
        
        JOptionPane.showMessageDialog(this, "New Prison is added.");

        txtPrisonId.setText("");
        txtPrisonName.setText("");
        txtOfficerId.setText("");
        txtOfficerName.setText("");
        txtJailCount.setText("");
        txtCity.setText("");          
        
    }//GEN-LAST:event_btnAddActionPerformed

