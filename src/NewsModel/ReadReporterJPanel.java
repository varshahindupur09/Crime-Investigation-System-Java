    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed

        //        boolean isValid = Validation();
        //        if (isValid) {
            int selectedRowIndex = ReporterDirectoryTable.getSelectedRow();
            if (selectedRowIndex < 0) {
                JOptionPane.showMessageDialog(this, "Please select a row to update");
                return;
            }

            DefaultTableModel model = (DefaultTableModel) ReporterDirectoryTable.getModel();
            Reporter selectedReporter = (Reporter) model.getValueAt(selectedRowIndex, 0);
            String rpId = NewRpIdTxt.getText();
            String rpName = NewRpNameTxt.getText();
            String rpEmail = NewRpEmailTxt.getText();
            int rpAge = Integer.parseInt(NewRpAgeTxt.getText());
            String rpGender = NewRpGenderTxt.getText();
            String rpDoj = NewRpDojTxt.getText();

            JOptionPane.showMessageDialog(this, "Doctor Information Updated");
            //history.deleteEmployee(selectedEmployee);
            selectedReporter.setReporterId(rpId);
            selectedReporter.setReporterName(rpName);
            selectedReporter.setReporterEmail(rpEmail);
            selectedReporter.setReporterGender(rpGender);
            selectedReporter.setReporterAge(rpAge);
            selectedReporter.setReporterDateOfJoining(rpDoj);

            populateTable();

            NewRpIdTxt.setText("");
            NewRpNameTxt.setText("");
            NewRpEmailTxt.setText("");
            NewRpAgeTxt.setText("");
            NewRpGenderTxt.setText("");
            NewRpDojTxt.setText("");
            //}
    }//GEN-LAST:event_updateBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = ReporterDirectoryTable.getSelectedRow();

        if(selectedRowIndex <0){
            JOptionPane.showMessageDialog(this, "Please enter the row you want to delete");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) ReporterDirectoryTable.getModel();
        Reporter rp = (Reporter) model.getValueAt(selectedRowIndex,0);

        reporterHistory.deleteReporter(rp);

        JOptionPane.showMessageDialog(this, "Requested Record is Deleted");
        populateTable();
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void populateTable() {

        DefaultTableModel model = (DefaultTableModel) ReporterDirectoryTable.getModel();
        model.setRowCount(0);

        for (Reporter rp : reporterHistory.getReporterData()) {

            Object[] row = new Object[7];
            row[0] = rp;
            row[1] = rp.getReporterId();
            row[2] = rp.getReporterName();
            row[3] = rp.getReporterEmail();
            row[4] = rp.getReporterGender();
            row[5] = rp.getReporterDateOfJoining();
            row[6] = rp.getReporterAge();

            model.addRow(row);

        }
    }

