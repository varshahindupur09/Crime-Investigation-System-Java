    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        // TODO add your handling code here:

        int selectedRowIndex = ReporterDirectoryTable.getSelectedRow();
        if(selectedRowIndex <0){
            JOptionPane.showMessageDialog(this, "Please enter the row you want to view");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) ReporterDirectoryTable.getModel();
        Reporter reporter = (Reporter) model.getValueAt(selectedRowIndex,0);

        NewRpIdTxt.setText(String.valueOf(reporter.getReporterId()));
        NewRpNameTxt.setText(String.valueOf(reporter.getReporterName()));
        NewRpEmailTxt.setText(String.valueOf(reporter.getReporterEmail()));
        NewRpAgeTxt.setText(String.valueOf(reporter.getReporterAge()));
        NewRpGenderTxt.setText(String.valueOf(reporter.getReporterGender()));
        NewRpDojTxt.setText(String.valueOf(reporter.getReporterDateOfJoining()));
    }//GEN-LAST:event_viewBtnActionPerformed

