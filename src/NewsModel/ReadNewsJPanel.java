    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed

        //        boolean isValid = Validation();
        //        if (isValid) {
            int selectedRowIndex = NewsDirectoryTable.getSelectedRow();
            if (selectedRowIndex < 0) {
                JOptionPane.showMessageDialog(this, "Please select a row to update");
                return;
            }

            DefaultTableModel model = (DefaultTableModel) NewsDirectoryTable.getModel();
            News selectedNews = (News) model.getValueAt(selectedRowIndex, 0);
            String nwId = NewNewsIdTxt.getText();
            String nwCrime = NewNewsCrimeTxt.getText();
            String nwCrimeDate = NewNewsCrimeDate.getText();
            String nwAuthor = NewNewsAuthorTxt.getText();
            String nwReporter = NewNewsReporterTxt.getText();
            String nwPublication = NewNewsPublicationTxt.getText();

            JOptionPane.showMessageDialog(this, "News Information Updated");
            //history.deleteEmployee(selectedEmployee);
            selectedNews.setNewsId(nwId);
            selectedNews.setNewsCrime(nwCrime);
            selectedNews.setNewsCrimeDate(nwCrimeDate);
            selectedNews.setNewsAuthor(nwAuthor);
            selectedNews.setNewsReporter(nwReporter);
            selectedNews.setNewsPublication(nwPublication);
            
            dbConNewsDetails.updateNewsDataToDB(selectedNews);
            populateTable();

            NewNewsIdTxt.setText("");
            NewNewsCrimeTxt.setText("");
            NewNewsCrimeDate.setText("");
            NewNewsAuthorTxt.setText("");
            NewNewsReporterTxt.setText("");
            NewNewsPublicationTxt.setText("");
            //}
    }//GEN-LAST:event_updateBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = NewsDirectoryTable.getSelectedRow();

        if(selectedRowIndex <0){
            JOptionPane.showMessageDialog(this, "Please enter the row you want to delete");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) NewsDirectoryTable.getModel();
        News news = (News) model.getValueAt(selectedRowIndex,0);

        nwHistory.deleteNews(news);
        
        dbConNewsDetails.deleteNewsDataInDB(news);
        JOptionPane.showMessageDialog(this, "Requested Record is Deleted");
        populateTable();
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void populateTable() {

        DefaultTableModel model = (DefaultTableModel) NewsDirectoryTable.getModel();
        model.setRowCount(0);

        for (News nw : nwHistory.getAuthData()) {

            Object[] row = new Object[7];
            row[0] = nw;
            row[1] = nw.getNewsCrime();
            row[2] = nw.getNewsCrimeDate();
            row[3] = nw.getNewsAuthor();
            row[4] = nw.getNewsReporter();
            row[5] = nw.getNewsPublication();
            

            model.addRow(row);

        }
    }
    
