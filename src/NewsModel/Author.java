public class Author {
    
    private String authorId;
    private String authorName;
    private int authorAge;
    private int authorYearsOfExperience;
    private String authorGender;
    private String authorDateOfJoining;

    public String getAuthorId() {
        return authorId;
    }

    public void setAuthorId(String authorId) {
        this.authorId = authorId;
    }

    public String getAuthorName() {
        return authorName;
    }

    public void setAuthorName(String authorName) {
        this.authorName = authorName;
    }

    public int getAuthorAge() {
        return authorAge;
    }

    public void setAuthorAge(int authorAge) {
        this.authorAge = authorAge;
    }

    public int getAuthorYearsOfExperience() {
        return authorYearsOfExperience;
    }

    public void setAuthorYearsOfExperience(int authorYearsOfExperience) {
        this.authorYearsOfExperience = authorYearsOfExperience;
    }

    public String getAuthorGender() {
        return authorGender;
    }

    public void setAuthorGender(String authorGender) {
        this.authorGender = authorGender;
    }

    public String getAuthorDateOfJoining() {
        return authorDateOfJoining;
    }

    public void setAuthorDateOfJoining(String authorDateOfJoining) {
        this.authorDateOfJoining = authorDateOfJoining;
    }
    
    
    
}
