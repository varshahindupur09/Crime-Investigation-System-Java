public class News {
    
    private String newsId;
    private String newsCrime;
    private String newsCrimeDate;
    private String newsAuthor;
    private String newsReporter;
    private String newsPublication;

    public String getNewsId() {
        return newsId;
    }

    public void setNewsId(String newsId) {
        this.newsId = newsId;
    }

    public String getNewsCrime() {
        return newsCrime;
    }

    public void setNewsCrime(String newsCrime) {
        this.newsCrime = newsCrime;
    }

    public String getNewsCrimeDate() {
        return newsCrimeDate;
    }

    public void setNewsCrimeDate(String newsCrimeDate) {
        this.newsCrimeDate = newsCrimeDate;
    }

    public String getNewsAuthor() {
        return newsAuthor;
    }

    public void setNewsAuthor(String newsAuthor) {
        this.newsAuthor = newsAuthor;
    }

    public String getNewsReporter() {
        return newsReporter;
    }

    public void setNewsReporter(String newsReporter) {
        this.newsReporter = newsReporter;
    }

    public String getNewsPublication() {
        return newsPublication;
    }

    public void setNewsPublication(String newsPublication) {
        this.newsPublication = newsPublication;
    }
      
    
}
