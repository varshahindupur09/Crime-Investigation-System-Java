public class Witness {
    
    private String witnessID;
    private String witnessName;
    private String witnessFatherName;
    private long witnessContactNo;
    private String witnessRemark;

    public String getWitnessID() {
        return witnessID;
    }

    public void setWitnessID(String witnessID) {
        this.witnessID = witnessID;
    }

    public String getWitnessName() {
        return witnessName;
    }

    public void setWitnessName(String witnessName) {
        this.witnessName = witnessName;
    }

    public String getWitnessFatherName() {
        return witnessFatherName;
    }

    public void setWitnessFatherName(String witnessFatherName) {
        this.witnessFatherName = witnessFatherName;
    }

    public long getWitnessContactNo() {
        return witnessContactNo;
    }

    public void setWitnessContactNo(long witnessContactNo) {
        this.witnessContactNo = witnessContactNo;
    }

    public String getWitnessRemark() {
        return witnessRemark;
    }

    public void setWitnessRemark(String witnessRemark) {
        this.witnessRemark = witnessRemark;
    }
    
    
    
}
