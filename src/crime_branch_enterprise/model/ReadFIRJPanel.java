    private void populateTable() {

        DefaultTableModel model = (DefaultTableModel) FIRDirectoryTable.getModel();
        model.setRowCount(0);

        for (FIR fir : firDir.getFIRHistory()) {

            Object[] row = new Object[9];
            row[0] = fir;
            row[1] = fir.getFirstname();
            row[2] = fir.getLastname();
            row[3] = fir.getDateOfOffence();
            row[4] = fir.getAddress();
            row[5] = fir.getEmailId();
            row[6] = fir.getPoliceStationLoc();
            row[7] = fir.getPhoneNum();
            row[8] = fir.getAccorvic();

            model.addRow(row);

        }
    }

